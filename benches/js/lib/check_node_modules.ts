/**
 * Preflight for the harness dependencies (`benches/js/node_modules`, installed
 * by `deno task bench:install`) — shared by the bench, smoke, and every
 * corpus/conformance comparison entry point (plus `scripts/doctor.ts` via the
 * non-exiting probe).
 *
 * Missing → fatal with the installer hint: without it the canonical impls
 * (prettier + svelte/compiler + acorn) fail later with an opaque
 * module-resolution error. Stale → fatal too: npm rewrites
 * `node_modules/.package-lock.json` on every install, so a `package.json`
 * newer than that stamp means the pins changed without a reinstall — the run
 * would use the OLD installed versions while reports label them with the new
 * pins. `BENCH_STALE_OK=1` downgrades stale (never missing) to a warning,
 * mirroring `check_artifact_freshness.ts`.
 *
 * Runtime-neutral (node: builtins only) — runs under Deno, Node, and Bun.
 */

import { stat } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import process from 'node:process';

export type NodeModulesStatus =
	| { status: 'ok' }
	| { status: 'missing'; message: string }
	| { status: 'stale'; message: string };

/** Inspect `benches/js/node_modules` without side effects (doctor-friendly). */
export async function probe_node_modules(): Promise<NodeModulesStatus> {
	const node_modules = fileURLToPath(new URL('../node_modules', import.meta.url));
	try {
		await stat(node_modules);
	} catch {
		return {
			status: 'missing',
			message:
				`Harness dependencies not installed (${node_modules} missing). ` +
				"Run 'deno task bench:install' first — it installs the npm deps + the oxc wasi binding.",
		};
	}

	// npm's install stamp; absent on exotic layouts — freshness is unknowable then.
	let stamp_mtime: number;
	try {
		const stamp = await stat(
			fileURLToPath(new URL('../node_modules/.package-lock.json', import.meta.url)),
		);
		stamp_mtime = stamp.mtimeMs;
	} catch {
		return { status: 'ok' };
	}
	const pkg = await stat(fileURLToPath(new URL('../package.json', import.meta.url)));
	if (pkg.mtimeMs <= stamp_mtime) return { status: 'ok' };
	return {
		status: 'stale',
		message:
			'benches/js/package.json is newer than the installed node_modules — ' +
			"re-run 'deno task bench:install' so the run matches the pins",
	};
}

/** Verify `benches/js/node_modules` exists and is not stale; exits the process otherwise. */
export async function check_node_modules(): Promise<void> {
	const probe = await probe_node_modules();
	if (probe.status === 'ok') return;
	if (probe.status === 'stale' && process.env.BENCH_STALE_OK === '1') {
		console.error(`⚠ ${probe.message} (proceeding: BENCH_STALE_OK=1).`);
		return;
	}
	const escape = probe.status === 'stale' ? ' (BENCH_STALE_OK=1 to proceed anyway)' : '';
	console.error(`\n✗ ${probe.message}${escape}\n`);
	process.exit(1);
}
